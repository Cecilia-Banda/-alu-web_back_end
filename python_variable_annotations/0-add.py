#!/usr/bin/env python3
"""
Module for python variable annotations
"""

def add(a: float, b: float) -> float:
    """
    A sum of two different floats
    
    Parameters:
    a (float): The first float.
    b (float): The second float.
    
    Returns:
    float: The sum of the two floats.
    """
    return a + b
